INSERT INTO tickets (event_id, ticket_type_id, attendee_id, seat_number, purchase_date, status, is_vip)
SELECT 
    tt.event_id,
    tt.id as ticket_type_id,
    1 + (FLOOR(RAND() * 200)) as attendee_id,
    CASE 
        WHEN tt.type_name LIKE '%VIP%' THEN CONCAT('VIP-', LPAD(FLOOR(RAND() * 100), 3, '0'))
        WHEN tt.type_name LIKE '%Floor%' OR tt.type_name LIKE '%GA%' THEN 'GA'
        ELSE CONCAT('SEC-', CHAR(65 + FLOOR(RAND() * 10)), '-', LPAD(FLOOR(RAND() * 50), 2, '0'))
    END as seat_number,
    DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 90) DAY) as purchase_date,
    CASE 
        WHEN RAND() > 0.1 THEN 'paid'
        WHEN RAND() > 0.5 THEN 'reserved'
        ELSE 'used'
    END as status,
    tt.type_name LIKE '%VIP%' as is_vip
FROM ticket_types tt
JOIN (
    SELECT 1 as n UNION SELECT 2 UNION SELECT 3 UNION SELECT 4 UNION SELECT 5 UNION
    SELECT 6 UNION SELECT 7 UNION SELECT 8 UNION SELECT 9 UNION SELECT 10 UNION
    SELECT 11 UNION SELECT 12 UNION SELECT 13 UNION SELECT 14 UNION SELECT 15 UNION
    SELECT 16 UNION SELECT 17 UNION SELECT 18 UNION SELECT 19 UNION SELECT 20
) numbers
WHERE tt.event_id <= 30  -- More tickets for first 30 events
AND numbers.n <= GREATEST(5, FLOOR(tt.quantity_available * 0.3));

UPDATE ticket_types tt
JOIN (
    SELECT ticket_type_id, COUNT(*) as sold
    FROM tickets
    GROUP BY ticket_type_id
) t ON tt.id = t.ticket_type_id
SET tt.quantity_available = tt.quantity_available - t.sold;

DELIMITER //
CREATE TRIGGER update_ticket_type_quantity
AFTER INSERT ON tickets
FOR EACH ROW
BEGIN
    UPDATE ticket_types
    SET quantity_available = quantity_available - 1
    WHERE id = NEW.ticket_type_id
    AND quantity_available > 0;
END//
DELIMITER ;


