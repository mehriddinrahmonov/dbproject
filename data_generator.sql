        WHEN 2 THEN 'PayPal'
        WHEN 3 THEN 'Apple Pay'
        WHEN 4 THEN 'Google Pay'
        ELSE 'Credit Card'
    END as payment_method,
    t.purchase_date as payment_date,
    CASE 
        WHEN RAND() > 0.7 THEN FLOOR(1 + RAND() * 5)
        ELSE NULL 
    END as promo_code_id,
    CONCAT('TXN-', LPAD(FLOOR(RAND() * 999999), 6, '0'), '-', UNIX_TIMESTAMP()) as transaction_id,
    CASE 
        WHEN t.status IN ('paid', 'used') THEN 'completed'
        WHEN t.status = 'reserved' THEN 'pending'
        ELSE 'completed'
    END as status
FROM tickets t
JOIN ticket_types tt ON t.ticket_type_id = tt.id
WHERE t.status IN ('paid', 'used', 'reserved')
LIMIT 400;

INSERT INTO payment_tickets (payment_id, ticket_id)
SELECT 
    p.id as payment_id,
    t.id as ticket_id
FROM payments p
JOIN tickets t ON t.attendee_id = p.attendee_id
WHERE t.status IN ('paid', 'used')
AND NOT EXISTS (
    SELECT 1 FROM payment_tickets pt 
    WHERE pt.ticket_id = t.id
)
LIMIT 500;


